def _sorted_records(records: list, kind: str) -> list:
    """Filter records by kind."""
    return [r for r in records if r.kind == kind]


class NumberedCanvas(canvas.Canvas):
    """Custom canvas for page numbering and footer."""

    def __init__(self, *args, **kwargs):
        canvas.Canvas.__init__(self, *args, **kwargs)
        self._saved_page_states = []

    def showPage(self):
        self._saved_page_states.append(dict(self.__dict__))
        self._startPage()

    def save(self):
        num_pages = len(self._saved_page_states)
        for state in self._saved_page_states:
            self.__dict__.update(state)
            self.draw_page_number(num_pages)
            canvas.Canvas.showPage(self)
        canvas.Canvas.save(self)

    def draw_page_number(self, page_count):
        """Add page number and footer to each page."""
        self.setFont("Helvetica", 8)
        self.setFillColor(COLOR_TEXT_SECONDARY)
        page_num = f"Page {self._pageNumber} of {page_count}"
        self.drawRightString(7.5 * inch, 0.5 * inch, page_num)
        self.drawString(1 * inch, 0.5 * inch, "Generated by EPM Wizard")


