def _create_cube_architecture_diagram(cubes_data: list, dims_data: list):
    """Create visual diagram showing cube-dimension relationships using drawing primitives."""
    # Limit to first 5 cubes for clarity
    display_cubes = cubes_data[:5]

    if not display_cubes:
        return None

    # Calculate dimensions
    cube_width = 120
    cube_height = 30
    dim_width = 100
    dim_height = 20
    h_spacing = 40
    v_spacing = 15

    # Build dimension set
    all_dims = set()
    for cube in display_cubes:
        data = cube.data or {}
        dims = data.get("dimensions") or []
        for dim in dims[:6]:  # Limit dimensions per cube
            all_dims.add(dim)

    dims_list = sorted(all_dims)[:10]  # Limit total dimensions shown

    # Calculate drawing size
    drawing_width = 500
    drawing_height = len(display_cubes) * (cube_height + v_spacing) + 100

    drawing = Drawing(drawing_width, drawing_height)

    # Draw cubes on the left
    cube_y_positions = {}
    y_pos = drawing_height - 50

    for cube in display_cubes:
        # Cube rectangle
        drawing.add(Rect(
            20, y_pos, cube_width, cube_height,
            fillColor=COLOR_PRIMARY,
            strokeColor=COLOR_PRIMARY,
            strokeWidth=2
        ))

        # Cube label
        label = cube.name[:15] + '...' if len(cube.name) > 15 else cube.name
        drawing.add(String(
            25, y_pos + cube_height / 2,
            label,
            fontSize=9,
            fillColor=colors.white,
            fontName='Helvetica-Bold',
            textAnchor='start'
        ))

        cube_y_positions[cube.name] = y_pos + cube_height / 2
        y_pos -= (cube_height + v_spacing)

    # Draw dimensions on the right
    dim_y_positions = {}
    y_pos = drawing_height - 50
    dim_x = drawing_width - dim_width - 20

    for dim in dims_list:
        # Dimension rectangle
        drawing.add(Rect(
            dim_x, y_pos, dim_width, dim_height,
            fillColor=COLOR_SUCCESS,
            strokeColor=COLOR_SUCCESS,
            strokeWidth=1
        ))

        # Dimension label
        label = dim[:12] + '...' if len(dim) > 12 else dim
        drawing.add(String(
            dim_x + 5, y_pos + dim_height / 2,
            label,
            fontSize=8,
            fillColor=colors.white,
            fontName='Helvetica',
            textAnchor='start'
        ))

        dim_y_positions[dim] = y_pos + dim_height / 2
        y_pos -= (dim_height + 10)

    # Draw connecting lines
    for cube in display_cubes:
        data = cube.data or {}
        dims = data.get("dimensions") or []
        cube_y = cube_y_positions[cube.name]

        for dim in dims:
            if dim in dim_y_positions:
                dim_y = dim_y_positions[dim]

                # Draw line from cube to dimension
                drawing.add(Line(
                    20 + cube_width, cube_y,
                    dim_x, dim_y,
                    strokeColor=COLOR_BORDER,
                    strokeWidth=0.5,
                    strokeDashArray=[2, 2]
                ))

    # Add legend
    legend_y = 20
    drawing.add(Rect(20, legend_y, 15, 10, fillColor=COLOR_PRIMARY, strokeColor=None))
    drawing.add(String(40, legend_y + 5, 'Cubes', fontSize=8, fillColor=COLOR_TEXT_PRIMARY))

    drawing.add(Rect(100, legend_y, 15, 10, fillColor=COLOR_SUCCESS, strokeColor=None))
    drawing.add(String(120, legend_y + 5, 'Dimensions', fontSize=8, fillColor=COLOR_TEXT_PRIMARY))

    return drawing


