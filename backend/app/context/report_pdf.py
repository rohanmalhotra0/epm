def _create_hierarchy_tree_drawing(members: list, max_depth: int = 3):
    """Create a visual tree drawing of member hierarchies."""
    by_name = {m.name: m for m in members}
    children: dict[str | None, list] = {}
    for m in members:
        parent = m.parent if (m.parent and m.parent in by_name and m.parent != m.name) else None
        children.setdefault(parent, []).append(m)

    roots = children.get(None, [])[:2]  # Limit to 2 roots for better layout

    if not roots:
        return None

    # Calculate tree layout with better spacing
    node_height = 25
    node_width = 160
    v_spacing = 30
    indent_per_level = 25

    # Count total visible nodes
    def count_nodes(node, depth):
        if depth >= max_depth:
            return 1
        count = 1
        node_children = children.get(node.name, [])[:4]  # Max 4 children per node
        for child in node_children:
            count += count_nodes(child, depth + 1)
        return count

    total_nodes = sum(count_nodes(root, 0) for root in roots)
    total_height = total_nodes * v_spacing + 40
    drawing = Drawing(500, min(total_height, 450))  # Cap height

    y_cursor = [drawing.height - 30]

    def draw_node(node, depth, parent_x=None, parent_y=None):
        if depth >= max_depth or y_cursor[0] < 20:
            return

        y = y_cursor[0]
        x = 20 + (depth * indent_per_level)
        y_cursor[0] -= v_spacing

        # Choose color based on depth
        if depth == 0:
            box_color = COLOR_PRIMARY
        elif depth == 1:
            box_color = COLOR_SUCCESS
        else:
            box_color = COLOR_TEXT_SECONDARY

        # Draw connecting line from parent
        if parent_x is not None and parent_y is not None:
            drawing.add(Line(
                parent_x + node_width, parent_y + node_height / 2,
                x, y + node_height / 2,
                strokeColor=COLOR_BORDER,
                strokeWidth=0.5,
                strokeDashArray=[2, 2]
            ))

        # Draw node box
        drawing.add(Rect(
            x, y, node_width, node_height,
            fillColor=box_color,
            strokeColor=box_color,
            strokeWidth=1
        ))

        # Node label - truncate long names
        label = node.name[:18] + '...' if len(node.name) > 18 else node.name
        drawing.add(String(
            x + 5, y + node_height / 2,
            label,
            fontSize=8,
            fillColor=colors.white,
            fontName='Helvetica',
            textAnchor='start'
        ))

        # Draw children (max 4)
        node_children = children.get(node.name, [])[:4]
        for child in node_children:
            draw_node(child, depth + 1, x, y)

    # Draw each root
    for root in roots:
        draw_node(root, 0)

    return drawing


def _create_members_section(members: list, styles):
    """Create the member hierarchies section with visual trees."""
    story = []

    story.append(Paragraph(
        f"Member Hierarchies ({len(members)} total)",
        styles['SectionHeading']
    ))

    if not members:
        story.append(Paragraph(
            "No members were captured. Members require an EPM Automate metadata "
            "export or LCM snapshot; run a context refresh once that is configured.",
            styles['BodyTextCustom']
        ))
        story.append(PageBreak())
        return story

    intro_text = (
        "Visual tree representation of dimension members showing parent-child "
        "relationships. Hierarchies are limited to 3 levels for readability."
    )
    story.append(Paragraph(intro_text, styles['BodyTextCustom']))
    story.append(Spacer(1, 0.2 * inch))

    # Group by dimension
    by_dim: dict[str, list] = {}
    for m in members:
        by_dim.setdefault(m.dimension or "—", []).append(m)

    # Show first 6 dimensions with hierarchies
    shown_dims = sorted(by_dim.keys())[:6]

    for dim in shown_dims:
        story.append(Paragraph(
            f"{dim} ({len(by_dim[dim])} members)",
            styles['SubsectionHeading']
        ))

        # Create visual tree
        tree_drawing = _create_hierarchy_tree_drawing(by_dim[dim])
        if tree_drawing:
            story.append(tree_drawing)
            story.append(Spacer(1, 0.2 * inch))

    if len(by_dim) > 6:
        remaining_count = sum(len(by_dim[d]) for d in by_dim if d not in shown_dims)
        story.append(Paragraph(
            f"... and {len(by_dim) - 6} more dimensions not shown ({remaining_count} members total)",
            styles['BodyTextCustom']
        ))

    story.append(PageBreak())

    return story


