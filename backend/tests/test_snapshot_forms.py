"""LCM snapshot form-definition parsing tests.

The shipped fixture snapshot carries only navigation-flow form *references*;
richer exports include real definition XML under ``Data Forms``. These tests
build synthetic zips whose definition files are generated by the artifact
renderer itself (``render_xml``), guaranteeing format fidelity with what the
parser round-trips, and prove: parsed FormRecords carry a full ``definition``,
a malformed file degrades to a name-only record plus an issue, nav-flow stubs
never duplicate a parsed definition, and the definition survives the context
merge into ``build_tenant_metadata`` for reference-form cloning.
"""

